package Ex03;

public class Basica {
	
	public static double Somar(double valA, double valB) {
		return valA + valB;
	}
	
	public static double Subtrair(double valA, double valB) {
		return valA - valB;
	}
	
	public static double Multiplicar(double valA, double valB) {
		return valA * valB;
	}
	
	public static double Dividir(double valA, double valB) {
		return valA/valB;
	}
	
	public static double Porcentagem(double valA, double valB) {
		return (valA*valB)/100;
	}
}
